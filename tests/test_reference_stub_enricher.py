class TestParseEnrichmentResponse:
    """Tests for the parse_enrichment_response() function."""

    def test_parses_well_formed_response(self):
        response = (
            "## Summary\n"
            "This is the summary.\n\n"
            "## Key Decisions\n"
            "- Decision one\n"
            "- Decision two\n\n"
            "## Key Interfaces\n"
            "- `foo()` -- does things\n"
            "- `bar()` -- does other things"
        )
        result = parse_enrichment_response(response)
        assert "summary" in result["summary"].lower() or "This is the summary" in result["summary"]
        assert "Decision one" in result["key_decisions"]
        assert "`foo()`" in result["key_interfaces"]

    def test_empty_response_returns_empty_sections(self):
        result = parse_enrichment_response("")
        assert result["summary"] == ""
        assert result["key_decisions"] == ""
        assert result["key_interfaces"] == ""

    def test_partial_response_fills_available_sections(self):
        response = "## Summary\nJust a summary, no other sections."
        result = parse_enrichment_response(response)
        assert "Just a summary" in result["summary"]
        assert result["key_decisions"] == ""
        assert result["key_interfaces"] == ""

    def test_case_insensitive_headers(self):
        response = (
            "## summary\n"
            "Lower case summary.\n\n"
            "## KEY DECISIONS\n"
            "- Upper case decision\n\n"
            "## Key Interfaces\n"
            "- Mixed case interface"
        )
        result = parse_enrichment_response(response)
        assert "Lower case summary" in result["summary"]
        assert "Upper case decision" in result["key_decisions"]
        assert "Mixed case interface" in result["key_interfaces"]

    def test_extra_headers_ignored(self):
        response = (
            "## Summary\n"
            "The summary.\n\n"
            "## Additional Notes\n"
            "These are extra notes.\n\n"
            "## Key Decisions\n"
            "- A decision\n\n"
            "## Key Interfaces\n"
            "- An interface"
        )
        result = parse_enrichment_response(response)
        assert "The summary" in result["summary"]
        assert "A decision" in result["key_decisions"]
        assert "An interface" in result["key_interfaces"]

    def test_multiline_content_preserved(self):
        response = (
            "## Summary\n"
            "Line one of summary.\n"
            "Line two of summary.\n"
            "Line three.\n\n"
            "## Key Decisions\n"
            "- Decision A\n"
            "- Decision B\n"
            "- Decision C\n\n"
            "## Key Interfaces\n"
            "- Interface X"
        )
        result = parse_enrichment_response(response)
        assert "Line one" in result["summary"]
        assert "Line three" in result["summary"]
        assert "Decision A" in result["key_decisions"]
        assert "Decision C" in result["key_decisions"]


