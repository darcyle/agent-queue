def _create_stub_file(vault_dir: str, project_id: str, stub_name: str) -> str:
    """Create a stub file with placeholder sections.

    Returns the path to the created stub file.
    """
    refs_dir = os.path.join(vault_dir, project_id, "references")
    os.makedirs(refs_dir, exist_ok=True)
    stub_path = os.path.join(refs_dir, stub_name)

    content = f"""\
---
tags: [spec, reference, auto-generated]
source: /workspace/specs/orchestrator.md
source_hash: abc123def456
last_synced: 2026-04-07
---

# Spec: Orchestrator

Full spec at `specs/orchestrator.md` in the {project_id} workspace.

## Excerpt

First few lines of the spec...

## Summary

*Summary pending -- will be generated by LLM enrichment.*

## Key Decisions

*Pending LLM extraction.*

## Key Interfaces

*Pending LLM extraction.*
"""
    with open(stub_path, "w", encoding="utf-8") as f:
        f.write(content)
    return stub_path


def _create_source_file(tmp_path, content: str = "") -> str:
    """Create a source spec file and return its path."""
    source = tmp_path / "workspace" / "specs" / "orchestrator.md"
    source.parent.mkdir(parents=True, exist_ok=True)
    if not content:
        content = """\
# Orchestrator Spec

## Overview
The orchestrator manages the main loop for task assignment.

## Design Decisions
- Tick-based loop at ~5s interval
- Single agent per task, no sharing
- Tasks assigned by priority then age

## Interfaces
- `Orchestrator.tick()` -- main loop entry
- `Orchestrator.assign_task()` -- agent selection
"""
    source.write_text(content, encoding="utf-8")
    return str(source)


