class TestUpdateStubContent:
    """Tests for the update_stub_content() function."""

    def test_replaces_placeholder_summary(self):
        existing = (
            "# Spec: Foo\n\n"
            "## Excerpt\n\nSome excerpt.\n\n"
            "## Summary\n\n"
            "*Summary pending -- will be generated by LLM enrichment.*\n\n"
            "## Key Decisions\n\n"
            "*Pending LLM extraction.*\n\n"
            "## Key Interfaces\n\n"
            "*Pending LLM extraction.*\n"
        )
        sections = {
            "summary": "This is the real summary.",
            "key_decisions": "- Real decision",
            "key_interfaces": "- `real_fn()` -- does stuff",
        }
        result = update_stub_content(existing, sections)
        assert "This is the real summary." in result
        assert "- Real decision" in result
        assert "`real_fn()`" in result
        assert "*Summary pending" not in result
        assert "*Pending LLM extraction.*" not in result

    def test_preserves_frontmatter(self):
        existing = (
            "---\ntags: [spec]\nsource: /foo\n---\n\n"
            "# Spec: Bar\n\n"
            "## Excerpt\n\nExcerpt text.\n\n"
            "## Summary\n\n*Placeholder*\n\n"
            "## Key Decisions\n\n*Placeholder*\n\n"
            "## Key Interfaces\n\n*Placeholder*\n"
        )
        sections = {"summary": "New summary.", "key_decisions": "", "key_interfaces": ""}
        result = update_stub_content(existing, sections)
        assert "---\ntags: [spec]\nsource: /foo\n---" in result
        assert "New summary." in result
        # Empty sections should NOT replace existing content
        assert "*Placeholder*" in result

    def test_empty_sections_preserve_existing(self):
        existing = (
            "## Summary\n\nExisting summary.\n\n"
            "## Key Decisions\n\n- Existing decision\n\n"
            "## Key Interfaces\n\n- Existing interface\n"
        )
        sections = {"summary": "", "key_decisions": "", "key_interfaces": ""}
        result = update_stub_content(existing, sections)
        assert result == existing

    def test_preserves_excerpt_section(self):
        existing = (
            "## Excerpt\n\nFirst lines of the file...\n\n"
            "## Summary\n\n*Placeholder*\n\n"
            "## Key Decisions\n\n*Placeholder*\n\n"
            "## Key Interfaces\n\n*Placeholder*\n"
        )
        sections = {
            "summary": "New summary.",
            "key_decisions": "- New decision",
            "key_interfaces": "- New interface",
        }
        result = update_stub_content(existing, sections)
        assert "First lines of the file..." in result
        assert "New summary." in result


